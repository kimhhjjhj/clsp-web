/**
 * 텍스트를 절(clause) 단위로 분리.
 * - 줄바꿈·쉼표·마침표·중점·불릿·세미콜론·파이프 기준
 * - 빈 절·공백 절 제거
 */
export function splitClauses(text: string): string[] {
  return text
    .split(/[\n\r\t·,.;|/]/g)
    .map(s => s.trim())
    .filter(s => s.length > 0)
}

/** 한 절이 규칙(AND)을 만족하는지 */
function clauseMatches(clause: string, rule: Rule): boolean {
  for (const kw of rule) {
    if (!clause.includes(kw)) return false
  }
  return true
}

/**
 * 텍스트에서 해당 공종 매칭 여부.
 * 어느 한 절이라도 어느 한 규칙을 만족하면 true.
 */
export function matchesTask(text: string, taskName: string): boolean {
  const rules = CPDB_RULES[taskName]
  if (!rules || rules.length === 0) return false
  const clauses = splitClauses(text)
  for (const c of clauses) {
    for (const r of rules) {
      if (clauseMatches(c, r)) return true
    }
  }
  return false
}

/** display용 — 규칙 배열을 'A+B, C+D' 형식 문자열 배열로 */
export function rulesSummary(taskName: string): string[] {
  const rules = CPDB_RULES[taskName]
  if (!rules) return []
  return rules.map(r => r.join('+'))
}
