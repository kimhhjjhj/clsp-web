/** (공종, 지역, 월0~11) → 기상여건 비작업일수 */
export function climateNonWorkDays(category: WorkCategory, region: Region, month0: number): number {
  const m = DATA_BY_CATEGORY[category]
  if (!m) return 5
  const arr = m[region] ?? m['서울']
  if (!arr || month0 < 0 || month0 > 11) return 5
  return arr[month0]
}

/** (공종, 지역) 연간 기상여건 비작업일수 합계 */
export function climateNonWorkDaysPerYear(category: WorkCategory, region: Region): number {
  const m = DATA_BY_CATEGORY[category]
  if (!m) return 60
  const arr = m[region] ?? m['서울']
  return arr.reduce((s, v) => s + v, 0)
}

/** 공종 문자열 → WorkCategory 매핑 (CP_DB category 호환) */
export function mapToWorkCategory(cat: string): WorkCategory {
  if (cat.includes('골조') || cat.includes('역타')) return '철근콘크리트'
  if (cat.includes('토목') || cat.includes('흙막이') || cat.includes('PRD')) return '토공사'
  if (cat.includes('철골')) return '철골공사'
  if (cat.includes('포장')) return '포장공사'
  if (cat.includes('마감') || cat.includes('공사준비')) return '옥내공사'
  return '철근콘크리트'
}
